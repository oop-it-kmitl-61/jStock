    private void tfPasswordKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tfPasswordKeyPressed
        // TODO add your handling code here:
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            String password = new User().getPassword();
            String inpPassword = String.valueOf(tfPassword.getPassword());

            if (password.equals(inpPassword)) {
                tfPassword.setText("");
                errMsg.setText(" ");
                doClose(RET_OK);
            } else {
                errMsg.setText("incorrect password");
            }
        }
    }//GEN-LAST:event_tfPasswordKeyPressed

    private void doClose(boolean retStatus) {
        returnStatus = retStatus;
        setVisible(false);
        dispose();
    }

