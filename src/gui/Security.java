    private void bnContinueActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bnContinueActionPerformed
        String password = new User().getPassword();
        String inpPassword = String.valueOf(tfPassword.getPassword());

        if (password.equals(inpPassword)) {
            tfPassword.setText("");
            errMsg.setText(" ");
            doClose(RET_OK);
        } else {
            errMsg.setText("incorrect password");
        }
    }//GEN-LAST:event_bnContinueActionPerformed

